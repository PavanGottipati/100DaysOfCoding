//Java Program to create String 
public class StringsCreation {
 public static void main(String [] args)
 {
    String str="Rakesh";
    System.out.println("The String created as an String literal is ");
    System.out.println(str);
    String st=new String("Kumar");
    System.out.println("The String created through new keyword i.e., Object is ");
    System.out.println(st);
 }   
}
